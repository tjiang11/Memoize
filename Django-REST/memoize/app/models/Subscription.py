class Subscription:
	
	def __init__(self):
		"""Create a new Subscription.
  		"""
		self.groups = []
		self.tags = []

	def subscribe_to_group(self, group):
		"""Create a new subscription to a group.

 		 Args:
	     	group: the group to add.
 		"""
		groups.append(group)

	def subscribe_to_tag(tag):
		"""Create a new subscription to a tag.

 		 Args:
	     	tag: the tag to add.
 		"""
		tags.append(tag)